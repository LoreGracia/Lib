#include "libft.h"

void	itohexup(unsigned int d, char **str, int i)
{
	char	*s;

	s = "0123456789ABCDEF";
	if (d > 15)
		itohexup(d / 16, str, i - 1);
	(*str)[i] = s[d % 16];
}
